package bbs;

public class Bbs {

	private int bbsID;
	private String bbsAni;
	private String bbsArea;
	private String bbsTitle;
	private String memberName;
	private String bbsDate;
	private String bbsContent;
	private String bbsImage;
	private String bbsRealImage;
	private int bbsAvailable;
	
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getBbsAni() {
		return bbsAni;
	}
	public void setBbsAni(String bbsAni) {
		this.bbsAni = bbsAni;
	}
	public String getBbsArea() {
		return bbsArea;
	}
	public void setBbsArea(String bbsArea) {
		this.bbsArea = bbsArea;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	
	public String getBbsDate() {
		return bbsDate;
	}
	public void setBbsDate(String bbsDate) {
		this.bbsDate = bbsDate;
	}
	public String getBbsContent() {
		return bbsContent;
	}
	public void setBbsContent(String bbsContent) {
		this.bbsContent = bbsContent;
	}
	public int getBbsAvailable() {
		return bbsAvailable;
	}
	public void setBbsAvailable(int bbsAvailable) {
		this.bbsAvailable = bbsAvailable;
	}
	public String getBbsImage() {
		return bbsImage;
	}
	public void setBbsImage(String bbsImage) {
		this.bbsImage = bbsImage;
	}
	public String getBbsRealImage() {
		return bbsRealImage;
	}
	public void setBbsRealImage(String bbsRealImage) {
		this.bbsRealImage = bbsRealImage;
	}
	
	
	
}
